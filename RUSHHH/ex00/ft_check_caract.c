int		ft_check_caract(char **argv)
{
	int	i;
	int	j;

	i = 0;
	while (argv[++i])
	{
		j = -1;
		while (argv[i][++j])
			if (argv[i][j] != '.' && (argv[i][j] > '9' || argv[i][j] < '1'))
				return (0);
		if (j != 9)
			return (0);
	}
	return (1);
}
