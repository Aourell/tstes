int		ft_check_y(char **argv, int y, char who)
{
	int	xx;
	int	tmp;

	xx = -1;
	tmp = 0;
	while (argv[y] && argv[y][++xx])
		if (argv[y][xx] == who)
			tmp++;
	return (tmp);
}
