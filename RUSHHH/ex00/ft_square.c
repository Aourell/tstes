#include <unistd.h>

void		print_square(char **tab)
{
	int		i;
	int		j;
	char	tmp;

	i = -1;
	while (tab[++i])
	{
		j = -1;
		while (tab[i][++j])
		{
			tmp = ((j < 8) ? ' ' : '\n');
			write(1, &tab[i][j], 1);
			write(1, &tmp, 1);
		}
	}
}
