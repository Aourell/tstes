int	ft_check_x(char **argv, int x, char who)
{
	int yy;
	int tmp;

	yy = 0;
	tmp = 0;
	while (argv[yy] != '\0')
	{
		if (argv[yy][x] == who)
			tmp++;
		yy++;
	}
	return (tmp);
}
