int	ft_check_y(char **argv, int y, char who)
{
	int	xx;
	int	tmp;

	xx = -1;
	tmp = 0;
	while (argv[y] != '\0' && argv[y][xx] != '\0')
	{
		if (argv[y][xx] == who)
			tmp++;
		xx++;
	}
	return (tmp);
}
