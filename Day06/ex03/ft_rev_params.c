void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int	i;
	int	j;

	argc = 0;
	i = 0;
	while (argv[i] != '\0')
		i++;
	i--;
	while (i >= 1)
	{
		while (argv[i][j] != '\0')
		{
			ft_putchar(argv[i][j]);
			j++;
		}
		ft_putchar('\n');
		j = 0;
		i--;
	}
}
