int		ft_atoi(char *str)
{
	int	i;
	int	nb;
	int n;

	i = 0;
	nb = 0;
	n = 0;
	while (str[i] == '\t' || str[i] == '\v' || str[i] == '\n' || \
			str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-')
		n = 1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10;
		nb = nb + str[i] - 48;
		i++;
	}
	if (n == 1)
		return (-nb);
	return (nb);
}
